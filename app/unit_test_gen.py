_BODY_FNS = {1: _body_t1, 2: _body_t2, 3: _body_t3, 4: _body_t4, 5: _body_t5, 6: _body_t3_xpath}

_SCREENSHOT_ACTIONS = frozenset({"verify_screenshot_gt", "verify_screenshot_diff"})


def _should_skip(test_num: int, entry: dict) -> bool:
    """Return True if the test for this entry would always be skipped."""
    if test_num in (2, 4):
        return not entry.get("hierarchy_file")
    if test_num in (1, 3):
        inp = entry.get("input", {})
        return inp.get("action") in _SCREENSHOT_ACTIONS and "bounds" not in inp
    if test_num == 6:
        return not entry.get("hierarchy_file_xpath")
    return False


# ── Public API ─────────────────────────────────────────────────────────────────

def generate_unit_tests(fixture_key: str, entry_count: int) -> List[str]:
    """Append test functions for each captured entry to the 5 gen test files.

    Args:
        fixture_key:  Folder name under test_unittest/fixtures/ (e.g. 'MyTest_20260603_143510').
        entry_count:  Number of entries in the capture (= number of recorded steps).

    Returns:
        Relative paths of test files that were written or updated.
    """
    import json as _json
    _TEST_UNITTEST.mkdir(exist_ok=True)

    capture_path = _TEST_UNITTEST / "fixtures" / fixture_key / "capture.json"
    entries = _json.loads(capture_path.read_text(encoding="utf-8"))["entries"] if capture_path.exists() else []

    updated: List[str] = []

    for test_num, (filename, desc) in _FILES.items():
        path = _TEST_UNITTEST / filename
        existing = path.read_text(encoding="utf-8") if path.exists() else _file_header(desc)

        new_funcs: List[str] = []
        for i in range(entry_count):
            fn_name = f"test_gen{test_num}_{fixture_key}_step_{i:03d}"
            if fn_name in existing:
                continue
            if i < len(entries) and _should_skip(test_num, entries[i]):
                continue
            new_funcs.append(_BODY_FNS[test_num](fixture_key, i))

        if not new_funcs:
            continue

        block_hdr = f"# ── {fixture_key}"
        additions = ""
        if block_hdr not in existing:
            bar = "─" * max(1, 74 - len(fixture_key))
            additions += f"\n\n{block_hdr} {bar}\n"
        additions += "".join(new_funcs)

        path.write_text(existing + additions, encoding="utf-8")
        rel = str(path.relative_to(_TEST_UNITTEST.parent))
        if rel not in updated:
            updated.append(rel)

    return updated
